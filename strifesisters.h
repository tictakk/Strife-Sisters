char *location_lookup(int position)
{
  switch(position)
  {

    case 994:
    return "Fort Begin";

    case 971:
    return "Port Here";

    case 876:
    return "Circle Stop";

    case 780:
    return "Castle Grr";

    case 713:
    return "Wonderton";

    case 611:
    return "Fort Scary";

    case 705:
    return "Smallton";

    case 807:
    return "Townsville";

    case 833:
    return "Wet Fork";

    case 903:
    return "Sea Views";

    default:
    return "No Location";
  }
}
